package com.swak.vertx.transport;

/**
 * 最简单的 Token
 * 
 * @author lifeng
 */
public class Token {

	private String name;
	private String token;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
}
