	// 转为JSon
	_toJson : function(o) {
		var that = this;
		var r = [];
		if (typeof o == "string")
			return "\""
					+ o.replace(/([\'\"\\])/g, "\\$1").replace(/(\n)/g, "\\n")
							.replace(/(\r)/g, "\\r").replace(/(\t)/g, "\\t")
					+ "\"";
		if (typeof o == "object") {
			if (!o.sort) {
				for ( var i in o)
					r.push(i + ":" + that._toJson(o[i]));
				r = "{" + r.join() + "}";
			} else {
				for (var i = 0; i < o.length; i++) {
					r.push(that._toJson(o[i]));
				}
				r = "[" + r.join() + "]";
			}
			return r;
		}
		return o.toString();
	},

	// 获得国家
	getCountrys : function() {
		var _countrys = [];
		var countrys = this['86'];
		for(var i in countrys) {
			var _c = countrys[i];
			_countrys.push.apply(_countrys, _c);
		}
		return this._toJson(_countrys);
	}
